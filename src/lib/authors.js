// Возвращает item с merged extras (fullBio/works/quotes).
function withDetails(author) {
  if (!author) return null;
  const extra = AUTHOR_DETAILS[author.slug];
  return extra ? { ...author, ...extra } : author;
}

export function getAuthorBySlug(slug) {
  return withDetails(AUTHORS.find((a) => a.slug === slug));
}

// Похожие авторы — те же tags (по числу пересечений), исключая текущего.
export function getRelatedAuthors(currentSlug, limit = 4) {
  const current = AUTHORS.find((a) => a.slug === currentSlug);
  if (!current) return [];
  const currentTags = new Set(current.tags ?? []);
  return AUTHORS
    .filter((a) => a.slug !== currentSlug)
    .map((a) => ({
      author: a,
      overlap: (a.tags ?? []).filter((t) => currentTags.has(t)).length,
    }))
    .filter((x) => x.overlap > 0)
    .sort((a, b) => b.overlap - a.overlap)
    .slice(0, limit)
    .map((x) => x.author);
}

export function authorHref(slug) {
  return `/authors/${slug}`;
}

// Поиск по name + role + bio + tags.
export function searchAuthors(items, query) {
  const q = query.trim().toLowerCase();
  if (!q) return items;
  return items.filter((a) => {
    const hay = [a.name, a.role, a.bio, ...(a.tags ?? [])]
      .filter(Boolean)
      .join(" ")
      .toLowerCase();
    return hay.includes(q);
  });
}
